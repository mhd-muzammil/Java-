package basic;

public class Operations {
    public static void main(String[] args) 
    {
        int a = 7;
        int b = 5;
        System.out.println(a + b); //addition
        System.out.println(a - b); //subtraction
        System.out.println(a * b); //multiplication
        System.out.println(a / b); //division it return the quotient of the values
        System.out.println(a % b); // modulus it return the remainder of the values
    }
}
