package basic;

public class Datatypes {
    public static void main(String [] args)
    {
        int a = 5;
        byte b = 6;
        short c = 7;
        long d = 8l;  // we must need to use L or l after the value because long is bigger than the value int
        float e = 9f; // we must need to use F or f after the value because By default, Java treats any decimal number as a double, not a float.
        double g = 10;
        char h = 'f';  //must use single quotes
        boolean i = true; // true or false

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
        
    }
}
