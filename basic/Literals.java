package basic;

public class Literals {
    public static void main(String[] args) {
        int a = 7; //7 is a int literal
        char c = 'd'; //d is a char literals
        double b = 10; //10 is a double literals
        boolean f = false; // false is a boolean literals

        System.out.println(a);
        System.out.println(c);
        System.out.println(b);
        System.out.println(f);

    }
}
